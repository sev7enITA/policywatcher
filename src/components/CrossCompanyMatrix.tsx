/**
 * Maps a KPI key + assessed value to a CSS module badge class.
 *
 * Special-case logic handles KPIs whose value semantics differ from
 * the global lookup tables (e.g. "Minimal" is good for Data Collection
 * but bad for Regulatory Compliance).
 *
 * @param kpiKey - KPI machine key.
 * @param value  - Assessed value string.
 * @returns CSS class name from the module stylesheet.
 */
const getBadgeClass = (kpiKey: string, value: string): string => {
  if (!value || value === 'Not assessed') return styles.badgeMuted;

  const v = value.toLowerCase();

  // Context-sensitive overrides: the same word can mean different things
  // depending on the KPI it describes.
  if (kpiKey === 'kpiDataCollection') {
    if (v === 'minimal') return styles.badgeGreen;
    if (v === 'moderate') return styles.badgeYellow;
    if (v === 'extensive') return styles.badgeRed;
  }

  if (kpiKey === 'kpiRegulatoryCompliance') {
    if (v === 'comprehensive') return styles.badgeGreen;
    if (v === 'partial') return styles.badgeYellow;
    if (v === 'minimal') return styles.badgeRed;
  }

  // Generic lookup tables for values that share consistent semantics
  // across all KPIs.
  const greenValues = [
    'none', 'available', 'full', 'restricted', 'published',
    'disclosed', 'committed', 'explicit opt-in', 'within 24h',
    'certified', 'transparent', 'user retained',
  ];

  const yellowValues = [
    'limited', 'extended', 'partial', 'controlled', 'shared',
    'opt-out', 'within 72h', 'mentioned',
  ];

  const redValues = [
    'broad', 'indefinite', 'not available', 'company claimed',
    'opaque', 'undisclosed', 'absent', 'implicit', 'unspecified',
  ];

  if (greenValues.includes(v)) return styles.badgeGreen;
  if (yellowValues.includes(v)) return styles.badgeYellow;
  if (redValues.includes(v)) return styles.badgeRed;

  return styles.badgeMuted;
};

/**
 * Returns 'green' | 'yellow' | 'red' | 'muted'
 */
const getBadgeColor = (kpiKey: string, value: string): string => {
  const cls = getBadgeClass(kpiKey, value);
  if (cls === styles.badgeGreen) return 'green';
  if (cls === styles.badgeYellow) return 'yellow';
  if (cls === styles.badgeRed) return 'red';
  return 'muted';
};

