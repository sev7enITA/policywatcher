/**
 * Full-screen gate that blocks access to the dashboard until the user
 * explicitly accepts the Terms of Use and Beta disclaimer.
 *
 * On acceptance, persists a flag to `localStorage` (`policywatcher_terms_accepted_v1`)
 * so returning visitors bypass the gate automatically.
 *
 * @param props - {@link TermsGateProps}
 * @returns The children if accepted, the gate overlay if not, or `null` during hydration.
 */
export default function TermsGate({ children, lang, onLangToggle }: TermsGateProps) {
  const [accepted, setAccepted] = useState<boolean | null>(null);
  const [checked, setChecked] = useState(false);
  const [showError, setShowError] = useState(false);

  // Read acceptance from localStorage on mount (client-only).
  // Checks both presence and TTL expiry (90 days).
  useEffect(() => {
    try {
      const v = localStorage.getItem(STORAGE_KEY);
      if (v) {
        const timestamp = parseInt(v, 10);
        if (!isNaN(timestamp) && Date.now() - timestamp < ACCEPTANCE_TTL_MS) {
          setAccepted(true);
          return;
        }
        // Expired or invalid: clear and re-prompt
        localStorage.removeItem(STORAGE_KEY);
      }
      setAccepted(false);
    } catch {
      // localStorage unavailable (SSR / privacy mode): default to not accepted
      setAccepted(false);
    }
  }, []);

  /**
   * Persists acceptance and unmounts the gate.
   * Requires the checkbox to be checked; shows an inline error otherwise.
   */
  const handleAccept = () => {
    if (!checked) {
      setShowError(true);
      return;
    }
    try {
      // Store acceptance timestamp for TTL expiry (90 days)
      localStorage.setItem(STORAGE_KEY, Date.now().toString());
    } catch {
      /* ignore write errors */
    }
    setAccepted(true);
  };

  // While we haven't read localStorage yet, render nothing (avoid flashing
  // the gate to users who already accepted).
  if (accepted === null) return null;

  if (accepted) return <>{children}</>;

  const t = content[lang];

  return (
    <div className={styles.overlay}>
      {/* Lang toggle */}
      <button onClick={onLangToggle} className={styles.langToggle}>
        {t.langToggle}
      </button>

      <div className={styles.card}>
        {/* Badge */}
        <div className={styles.badge}>
          <AlertTriangle size={13} />
          {t.badge}
        </div>

        {/* Header */}
        <h1 className={styles.title}>
          <ShieldCheck size={28} className={styles.titleIcon} />
          {t.title}
        </h1>
        <p className={styles.subtitle}>{t.subtitle}</p>

        {/* Terms box */}
        <div className={styles.termsBox}>
          <div className={styles.termsHeader}>
            <Lock size={14} />
            {t.termsTitle}
          </div>
          <ul className={styles.termsList}>
            {t.terms.map((term, i) => (
              <li key={i} className={styles.termsItem}>
                <span className={styles.termsBullet}>{i + 1}</span>
                <span>{term}</span>
              </li>
            ))}
          </ul>
        </div>

        {/* Checkbox */}
        <label className={styles.checkboxRow}>
          <input
            type="checkbox"
            checked={checked}
            onChange={(e) => {
              setChecked(e.target.checked);
              if (e.target.checked) setShowError(false);
            }}
            className={styles.checkbox}
          />
          <span className={styles.checkboxLabel}>{t.checkbox}</span>
        </label>
        {showError && (
          <p className={styles.error}>
            {lang === 'it'
              ? 'Devi spuntare la casella per continuare.'
              : 'You must check the box to continue.'}
          </p>
        )}

        {/* Actions */}
        <div className={styles.actions}>
          <button
            onClick={handleAccept}
            className={`${styles.btn} ${styles.btnPrimary} ${!checked ? styles.btnDisabled : ''}`}
            disabled={!checked}
          >
            <ShieldCheck size={16} />
            {t.acceptBtn}
          </button>
        </div>

        <p className={styles.footer}>
          <ExternalLink size={11} />
          {t.footer}
        </p>
      </div>
    </div>
  );
}
