'use client';

/**
 * TermsGate - blocks access to the platform until the user explicitly
 * accepts the Terms of Use and the Beta disclaimer.
 *
 * Persistence: the acceptance is stored in localStorage under
 * `policywatcher_terms_accepted_v1`. Bumping the suffix invalidates
 * previous acceptances (use when the terms text changes materially).
 *
 * Behaviour:
 *  - When NOT accepted: renders a full-screen gate and returns null for
 *    the children (the dashboard is NOT mounted).
 *  - When accepted: renders children normally.
 */
import { useEffect, useState } from 'react';
import { ShieldCheck, AlertTriangle, ExternalLink, Lock } from 'lucide-react';
import styles from './TermsGate.module.css';
import type { Lang } from '@/types';

const STORAGE_KEY = 'policywatcher_terms_accepted_v1';
/** Terms acceptance expires after 90 days (milliseconds). */
const ACCEPTANCE_TTL_MS = 90 * 24 * 60 * 60 * 1000;

/** Props for the {@link TermsGate} component. */
interface TermsGateProps {
  /** The application content that will only render after acceptance. */
  children: React.ReactNode;
  /** Active UI language — controls the terms copy and button labels. */
  lang: Lang;
  /** Toggles the language *before* acceptance (the gate renders its own toggle). */
  onLangToggle: () => void;
}

